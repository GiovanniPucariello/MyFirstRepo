package com.hp.team7.vo;

import java.io.Serializable;

public class DealerType implements Serializable {

	private static final long serialVersionUID = 1050618070194335158L;
	
	private int id;			//经销商类型ID
	private String name;	//经销商类型名字
	private String description;//经销商类型描述
	
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	
	
}
